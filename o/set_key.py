# 说明: 此文件可用来更改 parse_video 图形界面 的键盘 快捷键

# KEY_COPY_URLS		一键复制: 复制解析结果中的全部 URL 到剪切板, 默认快捷键是 F9
# 默认情况是 KEY_COPY_URLS = '<F9>', 请修改下面一行
KEY_COPY_URLS = '<F9>'

# KEY_PASTE_URL		一键粘贴: 粘贴 剪切板 中的文本到 文本框, 默认快捷键是 F8
# 默认情况是 KEY_PASTE_URL = '<F8>', 请修改下面一行
KEY_PASTE_URL = '<F8>'

# KEY_START_PARSE	一键解析: 开始解析, 默认快捷键是 F7
# 默认情况是 KEY_START_PARSE = '<F7>', 请修改下面一行
KEY_START_PARSE = '<F7>'

